# Base directory
BASE_DIR = Path(__file__).parent.parent
NUTUK_DIR = BASE_DIR / "training-data" / "nutuk-osmanli"
HISTORICAL_DIR = BASE_DIR / "training-data" / "real-historical"

def create_nutuk_page(page_num, title, content, word_count):
    """Nutuk sayfası oluştur"""
    filename = f"nutuk_page_{page_num:03d}_{title}"
    
    # Ground truth
    gt_path = NUTUK_DIR / "groundtruth" / f"{filename}.txt"
    with open(gt_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    # Metadata
    metadata = {
        "source": "Nutuk - Mustafa Kemal Atatürk",
        "original_publication": "1927",
        "language": "Ottoman Turkish (Arabic script)",
        "script": "Arabic",
        "page_number": page_num,
        "section": title,
        "license": "Public Domain",
        "character_count": len(content),
        "word_count": word_count,
        "historical_period": "1919-1927",
        "document_type": "Speech/Memoirs",
        "quality": "high"
    }
    
    meta_path = NUTUK_DIR / "metadata" / f"{filename}.json"
    with open(meta_path, 'w', encoding='utf-8') as f:
        json.dump(metadata, f, ensure_ascii=False, indent=2)
    
    print(f"✓ Oluşturuldu: {filename} ({len(content)} karakter)")

def create_historical_document(filename, title, year, content, doc_type):
    """Tarihi belge oluştur"""
    # Ground truth
    gt_path = HISTORICAL_DIR / "groundtruth" / f"{filename}.txt"
    with open(gt_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    # Metadata
    metadata = {
        "title": title,
        "year": year,
        "language": "Ottoman Turkish (Arabic script)",
        "script": "Arabic",
        "license": "Public Domain",
        "character_count": len(content),
        "document_type": doc_type,
        "historical_significance": "high",
        "quality": "high"
    }
    
    meta_path = HISTORICAL_DIR / "metadata" / f"{filename}.json"
    with open(meta_path, 'w', encoding='utf-8') as f:
        json.dump(metadata, f, ensure_ascii=False, indent=2)
    
    print(f"✓ Oluşturuldu: {filename} ({len(content)} karakter)")

